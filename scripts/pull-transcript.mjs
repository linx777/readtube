#!/usr/bin/env node

import { mkdir, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import {
  fetchTranscript,
  listLanguages,
  toPlainText,
  toSRT,
  toVTT,
} from 'youtube-transcript-plus';

const DEFAULT_FORMAT = 'text';
const VALID_FORMATS = new Set(['text', 'json', 'srt', 'vtt', 'timed']);

function printHelp() {
  console.error(`
Pull a YouTube transcript from a video URL or ID.

Usage:
  npm run transcript:pull -- <youtube-url-or-id> [options]
  node scripts/pull-transcript.mjs <youtube-url-or-id> [options]

Options:
  --lang <code>           Preferred transcript language, like en or zh-CN
  --format <type>         Output format: text, timed, json, srt, vtt
  --out <path>            Write output to a file instead of stdout
  --list-languages        Print available caption languages and exit
  --help                  Show this help

Examples:
  npm run transcript:pull -- https://www.youtube.com/watch?v=xRh2sVcNXQ8
  npm run transcript:pull -- xRh2sVcNXQ8 --format timed
  npm run transcript:pull -- xRh2sVcNXQ8 --format srt --out ./tmp/video.srt
  npm run transcript:pull -- xRh2sVcNXQ8 --list-languages
`.trim());
}

function parseArgs(argv) {
  const args = {
    input: '',
    lang: undefined,
    format: DEFAULT_FORMAT,
    out: undefined,
    listLanguages: false,
    help: false,
  };

  for (let index = 0; index < argv.length; index += 1) {
    const token = argv[index];

    if (!token.startsWith('--') && !args.input) {
      args.input = token;
      continue;
    }

    if (token === '--help') {
      args.help = true;
      continue;
    }

    if (token === '--list-languages') {
      args.listLanguages = true;
      continue;
    }

    if (token === '--lang') {
      const value = argv[index + 1];
      if (!value || value.startsWith('--')) {
        throw new Error('Missing value for --lang');
      }
      args.lang = value;
      index += 1;
      continue;
    }

    if (token === '--format') {
      const value = argv[index + 1];
      if (!value || value.startsWith('--')) {
        throw new Error('Missing value for --format');
      }
      if (!VALID_FORMATS.has(value)) {
        throw new Error(`Unsupported format "${value}". Use one of: ${[...VALID_FORMATS].join(', ')}`);
      }
      args.format = value;
      index += 1;
      continue;
    }

    if (token === '--out') {
      const value = argv[index + 1];
      if (!value || value.startsWith('--')) {
        throw new Error('Missing value for --out');
      }
      args.out = value;
      index += 1;
      continue;
    }

    throw new Error(`Unknown argument: ${token}`);
  }

  return args;
}

function formatClock(totalSeconds) {
  const rounded = Math.max(0, Math.floor(totalSeconds));
  const hours = Math.floor(rounded / 3600);
  const minutes = Math.floor((rounded % 3600) / 60);
  const seconds = rounded % 60;

  if (hours > 0) {
    return `${String(hours).padStart(2, '0')}:${String(minutes).padStart(2, '0')}:${String(seconds).padStart(2, '0')}`;
  }

  return `${String(minutes).padStart(2, '0')}:${String(seconds).padStart(2, '0')}`;
}

function toTimedText(segments) {
  return segments
    .map((segment) => `[${formatClock(segment.offset)}] ${segment.text}`)
    .join('\n');
}

function buildOutput(format, transcript) {
  switch (format) {
    case 'json':
      return JSON.stringify(transcript, null, 2);
    case 'srt':
      return toSRT(transcript.segments);
    case 'vtt':
      return toVTT(transcript.segments);
    case 'timed':
      return toTimedText(transcript.segments);
    case 'text':
    default:
      return toPlainText(transcript.segments);
  }
}

async function writeOutput(content, outputPath) {
  const absolutePath = resolve(outputPath);
  await mkdir(dirname(absolutePath), { recursive: true });
  await writeFile(absolutePath, content, 'utf8');
  return absolutePath;
}

async function main() {
  const args = parseArgs(process.argv.slice(2));

  if (args.help || !args.input) {
    printHelp();
    process.exit(args.help ? 0 : 1);
  }

  if (args.listLanguages) {
    const languages = await listLanguages(args.input, {
      retries: 2,
      retryDelay: 500,
    });

    const output = languages
      .map((language) => `${language.languageCode}\t${language.languageName}\t${language.isAutoGenerated ? 'auto' : 'manual'}`)
      .join('\n');

    console.log(output);
    return;
  }

  const transcript = await fetchTranscript(args.input, {
    lang: args.lang,
    retries: 2,
    retryDelay: 500,
    videoDetails: true,
  });

  const content = buildOutput(args.format, transcript);

  if (args.out) {
    const absolutePath = await writeOutput(content, args.out);
    console.error(`Saved ${args.format} transcript to ${absolutePath}`);
  } else {
    console.log(content);
  }

  console.error(
    `Video: ${transcript.videoDetails.title} | Language: ${args.lang ?? transcript.segments[0]?.lang ?? 'default'} | Segments: ${transcript.segments.length}`,
  );
}

main().catch((error) => {
  const message = error instanceof Error ? error.message : String(error);
  console.error(`Failed to pull transcript: ${message}`);
  process.exit(1);
});
