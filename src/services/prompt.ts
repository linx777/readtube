function buildTranscriptExcerpt(chunks: TranscriptChunk[], budget = FINAL_TRANSCRIPT_CONTEXT_LIMIT): string {
  const rendered = chunks.map((chunk) => `[${formatTime(chunk.start)}-${formatTime(chunk.end)}] ${chunk.text}`);
  const total = rendered.join('\n');
  if (total.length <= budget) {
    return total;
  }

  const selectedIndexes = new Set<number>();
  const edge = Math.min(6, chunks.length);

  for (let index = 0; index < edge; index += 1) {
    selectedIndexes.add(index);
  }

  for (let index = Math.max(0, chunks.length - edge); index < chunks.length; index += 1) {
    selectedIndexes.add(index);
  }

  const middleCount = Math.max(0, 12 - selectedIndexes.size);
  for (let slot = 0; slot < middleCount; slot += 1) {
    const ratio = (slot + 1) / (middleCount + 1);
    selectedIndexes.add(Math.floor((chunks.length - 1) * ratio));
  }

  return [...selectedIndexes]
    .sort((left, right) => left - right)
    .map((index) => rendered[index])
    .join('\n');
}

function buildVideoMeta(bundle: TranscriptBundle): string {
  return [
    `源视频标题：${bundle.sourceTitle}`,
    `频道：${bundle.sourceAuthor}`,
    `字幕语言：${bundle.languageName} (${bundle.languageCode})`,
    `字幕类型：${bundle.isAutoGenerated ? '自动字幕' : '人工或标准字幕'}`,
    `时长：${formatTime(bundle.durationSeconds)}`,
  ].join('\n');
}

export function buildCompressionPrompt(
  bundle: TranscriptBundle,
  transcriptText: string,
  batchLabel?: string,
): string {
  return [
    '你是一位中文科技媒体编辑，请把下面的 YouTube 对话整理成一份“成稿提纲”。',
    '要求：',
    '- 只能依据字幕，不要补写字幕里不存在的事实或判断。',
    '- 使用简体中文。',
    '- 输出 Markdown。',
    '- 保持压缩，但不要丢掉核心论点、追问关系和语气张力。',
    '- 包含以下四部分：',
    '## 核心结论',
    '- 4 到 6 条简洁 bullet',
    '## 议题展开',
    '- 按主题梳理 4 到 6 个部分',
    '## 关键张力',
    '- 写出主持人与嘉宾争论、追问、判断的重点',
    '## 可引用表达',
    '- 6 到 10 条值得做成引语的原话或近似转述',
    '',
    batchLabel ? `当前片段：${batchLabel}` : '',
    buildVideoMeta(bundle),
    '',
    '对话内容：',
    transcriptText,
  ]
    .filter(Boolean)
    .join('\n');
}

export function buildCompressionMergePrompt(bundle: TranscriptBundle, batchSummaries: string[]): string {
  return [
    '你是一位中文科技媒体编辑，请把下面多段“分段提纲”合并成一份最终总提纲。',
    '要求：',
    '- 只能依据已有提纲，不要补充新事实。',
    '- 使用简体中文。',
    '- 输出 Markdown。',
    '- 去重、合并相近观点，保留最重要的论点和对话张力。',
    '- 结构固定为：',
    '## 核心结论',
    '## 议题展开',
    '## 关键张力',
    '## 可引用表达',
    '',
    buildVideoMeta(bundle),
    '',
    '分段提纲：',
    batchSummaries.map((summary, index) => `### 第 ${index + 1} 段\n${summary}`).join('\n\n'),
  ].join('\n');
}

export function buildArticlePrompt(bundle: TranscriptBundle, condensedBrief?: string): string {
  const transcriptContext = condensedBrief
    ? [
        '长视频整理提纲：',
        condensedBrief,
        '',
        '供校准语气的原字幕摘录：',
        buildTranscriptExcerpt(bundle.chunks),
      ].join('\n')
    : `字幕全文：\n${bundle.transcriptText}`;

  return [
    '请基于下面的 YouTube 字幕，输出一篇中文 Markdown 访谈整理稿。',
    '',
    '要求：',
    '1. 保留原始对话关系。',
    '2. 按主题分节。',
    '3. 标题和小节标题要有中文媒体编辑感。',
    '4. 忠于原意，不要虚构。',
    '5. 只输出 Markdown 正文。',
    '6. 二级标题请尽量使用 ## [00:00-03:20] 小节标题 这种格式，时间范围来自字幕讨论区间。',
    '7. 问答段落请尽量使用 [00:32] Jen: ... / [01:08] Mark: ... 这种格式。',
    '',
    buildVideoMeta(bundle),
    '',
    transcriptContext,
  ].join('\n');
}

export function buildOpeningArticlePrompt(
  bundle: TranscriptBundle,
  transcriptText: string,
  windowEndLabel: string,
): string {
  return [
    '你是一位中文媒体的深度编辑，正在提前起草一篇长篇访谈稿的开头部分。',
    '你只看到了这场访谈前半段的一部分内容，请先写出可以成立的“开篇版本”。',
    '',
    '硬性要求：',
    '- 只根据已提供的前段字幕写作，不要预告后文中尚未出现的观点。',
    '- 使用简体中文。',
    '- 只输出 Markdown，不要输出 HTML、代码块、表格。',
    '- 第一行必须是 # 一级标题。',
    '- 标题后写一段简短导语。',
    '- 接着写 2 到 4 个 ## 小节。',
    '- 每个 ## 小节都必须带时间戳，格式固定为：## [00:00-03:20] 小节标题。',
    '- 每节正文用问答形式书写，格式固定为：[00:32] Jen: ... / [01:08] Mark: ...',
    '- 这是“提前开篇”，不要写总结收束。',
    '',
    buildVideoMeta(bundle),
    `当前只允许使用 00:00 到 ${windowEndLabel} 左右的对话内容。`,
    '',
    '可用字幕：',
    transcriptText,
  ].join('\n');
}

export function buildContinuationArticlePrompt(
  bundle: TranscriptBundle,
  laterTranscriptText: string,
  openingWindowEndLabel: string,
  condensedBrief?: string,
): string {
  const context = condensedBrief
    ? ['后半程整理提纲：', condensedBrief, '', '后半程字幕：', laterTranscriptText].join('\n')
    : `后半程字幕：\n${laterTranscriptText}`;

  return [
    '你是一位中文媒体编辑，正在继续补写一篇已经开始成型的长篇访谈稿。',
    '开头部分已经根据前段对话写完，现在请只补写后续章节。',
    '',
    '硬性要求：',
    '- 不要重复主标题，不要重新写文章导语。',
    '- 只补写新的 ## 小节和最后的收束段。',
    '- 不要重复前半段已经覆盖的话题；前半段已覆盖到大约 ' + openingWindowEndLabel + '。',
    '- 每个 ## 小节都必须带时间戳，格式固定为：## [10:20-14:08] 小节标题。',
    '- 每节正文继续使用问答形式：[时间] 发言人: 内容。',
    '- 最后只写一个收束段，不要再加“总结”标题。',
    '- 只根据提供的字幕或提纲补写，不要脑补。',
    '',
    buildVideoMeta(bundle),
    '',
    context,
  ].join('\n');
}
