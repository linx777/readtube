- 不使用列表，除非字幕本身明确在枚举要点
- 不使用粗体滥标
- 不输出结语、总结、编者按，除非字幕本身自然收束到总结段

质量标准：
- 读起来像一篇“编辑整理后的中文访谈稿”
- 不是逐句翻译稿
- 不是摘要
- 是保留原对话关系的高可读中文成稿
