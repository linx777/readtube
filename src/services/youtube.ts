function mergeVideoDetails(
  primary?: YoutubeVideoDetails,
  fallback?: YoutubeVideoDetails,
): YoutubeVideoDetails | undefined {
  if (!primary && !fallback) {
    return undefined;
  }

  const primaryThumbnails = (primary?.thumbnail?.thumbnails ?? [])
    .filter((thumbnail) => Boolean(thumbnail.url?.trim()))
    .map((thumbnail) => ({ url: thumbnail.url?.trim() }));
  const fallbackThumbnails = (fallback?.thumbnail?.thumbnails ?? [])
    .filter((thumbnail) => Boolean(thumbnail.url?.trim()))
    .map((thumbnail) => ({ url: thumbnail.url?.trim() }));

  return {
    title: hasMeaningfulVideoTitle(primary)
      ? primary?.title?.trim()
      : hasMeaningfulVideoTitle(fallback)
        ? fallback?.title?.trim()
        : primary?.title?.trim() || fallback?.title?.trim(),
    author: primary?.author?.trim() || fallback?.author?.trim(),
    channelId: primary?.channelId?.trim() || fallback?.channelId?.trim(),
    lengthSeconds: primary?.lengthSeconds?.trim() || fallback?.lengthSeconds?.trim(),
    viewCount: primary?.viewCount?.trim() || fallback?.viewCount?.trim(),
    thumbnail: primaryThumbnails.length
      ? { thumbnails: primaryThumbnails }
      : fallbackThumbnails.length
        ? { thumbnails: fallbackThumbnails }
        : undefined,
  };
}

export function extractVideoId(input: string): string {
  if (VIDEO_ID_PATTERN.test(input)) {
    return input;
  }

  const match = input.match(YOUTUBE_URL_PATTERN);
  if (match?.[1]) {
    return match[1];
  }

  throw new AppError('invalid_video_id', '这不是一个合法的 YouTube 视频链接。', 400);
}

function readTrackName(track: YoutubeCaptionTrack): string {
  if (track.name?.simpleText) {
    return track.name.simpleText;
  }

  if (track.name?.runs?.length) {
    return track.name.runs.map((item) => item.text ?? '').join('').trim();
  }

  return track.languageCode ?? 'Unknown';
}

function readTextValue(value: unknown): string {
  if (typeof value === 'string') {
    return normalizeWhitespace(value);
  }

  if (!value || typeof value !== 'object') {
    return '';
  }

  const record = value as Record<string, unknown>;
  if (typeof record.simpleText === 'string') {
    return normalizeWhitespace(record.simpleText);
  }

  if (Array.isArray(record.runs)) {
    return normalizeWhitespace(
      record.runs
        .map((item) => (
          item
          && typeof item === 'object'
          && typeof (item as { text?: unknown }).text === 'string'
        ) ? String((item as { text: string }).text) : '')
        .join(''),
    );
  }

  return '';
}

function isAutoTrack(track: YoutubeCaptionTrack): boolean {
  return track.kind === 'asr' || track.vssId?.startsWith('a.') === true;
}

function chooseCaptionTrack(tracks: YoutubeCaptionTrack[]): YoutubeCaptionTrack {
  const scoredTracks = [...tracks].sort((left, right) => scoreTrack(right) - scoreTrack(left));
  const track = scoredTracks[0];

  if (!track) {
    throw new AppError('missing_captions', '这个视频没有可用字幕。', 422);
  }

  return track;
}

function scoreTrack(track: YoutubeCaptionTrack): number {
  const languageCode = track.languageCode ?? '';
  let score = 0;

  if (!isAutoTrack(track)) {
    score += 100;
  }

  if (languageCode.startsWith('en')) {
    score += 60;
  }

  if (languageCode.startsWith('zh')) {
    score += 40;
  }

  if (track.kind !== 'forced') {
    score += 5;
  }

  return score;
}

function cleanSegmentText(text: string): string {
  const normalized = normalizeWhitespace(decodeXmlEntities(text));
  if (!normalized) {
    return '';
  }

  const lowered = normalized.toLowerCase();
  if (IGNORED_CUES.has(lowered)) {
    return '';
  }

  return normalized;
}

export function buildTranscriptChunks(segments: TranscriptSegment[]): TranscriptChunk[] {
  const chunks: TranscriptChunk[] = [];

  let current: TranscriptChunk | null = null;

  for (const segment of segments) {
    const text = cleanSegmentText(segment.text);
    if (!text) {
      continue;
    }

    const start = segment.offset;
    const end = segment.offset + segment.duration;

    if (!current) {
      current = { start, end, text };
      continue;
    }

    const gap = start - current.end;
    const nextLength = current.text.length + 1 + text.length;
    const shouldSplit =
      gap > 6 ||
      nextLength > 300 ||
      (current.text.length > 150 && /[.!?。！？]$/.test(current.text));

    if (shouldSplit) {
      chunks.push(current);
      current = { start, end, text };
      continue;
    }

    current.end = end;
    current.text = joinTranscriptText(current.text, text);
  }

  if (current) {
    chunks.push(current);
  }

  return chunks;
}

function joinTranscriptText(left: string, right: string): string {
  if (!left) {
    return right;
  }

  if (/[-/([]$/.test(left) || /^[,.;:!?)]/.test(right)) {
    return `${left}${right}`;
  }

  return `${left} ${right}`;
}

