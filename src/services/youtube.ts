function extractJsonObjectLiteral(source: string, marker: string): string | null {
  const markerIndex = source.indexOf(marker);
  if (markerIndex === -1) {
    return null;
  }

  const braceStart = source.indexOf('{', markerIndex + marker.length);
  if (braceStart === -1) {
    return null;
  }

  let depth = 0;
  let inString = false;
  let quoteChar = '"';
  let escaped = false;

  for (let index = braceStart; index < source.length; index += 1) {
    const char = source[index];

    if (inString) {
      if (escaped) {
        escaped = false;
        continue;
      }

      if (char === '\\') {
        escaped = true;
        continue;
      }

      if (char === quoteChar) {
        inString = false;
      }
      continue;
    }

    if (char === '"' || char === '\'') {
      inString = true;
      quoteChar = char;
      continue;
    }

    if (char === '{') {
      depth += 1;
      continue;
    }

    if (char === '}') {
      depth -= 1;
      if (depth === 0) {
        return source.slice(braceStart, index + 1);
      }
    }
  }

  return null;
}

function extractInitialPlayerResponse(watchHtml: string): Record<string, any> | null {
  const markers = [
    'var ytInitialPlayerResponse = ',
    'ytInitialPlayerResponse = ',
    'window["ytInitialPlayerResponse"] = ',
  ];

  for (const marker of markers) {
    const rawJson = extractJsonObjectLiteral(watchHtml, marker);
    if (!rawJson) {
      continue;
    }

    try {
      const parsed = JSON.parse(rawJson);
      if (parsed && typeof parsed === 'object' && !Array.isArray(parsed)) {
        return parsed as Record<string, any>;
      }
    } catch (error) {
      console.warn('[youtube] initial_player_response_parse_failed', JSON.stringify({
        marker,
        error: error instanceof Error ? error.message : String(error),
      }));
    }
  }

  return null;
}

