function decodeXmlEntities(text: string): string {
  return text.replace(/&(?:amp|lt|gt|quot|apos|#39);/g, (entity) => XML_ENTITIES[entity] ?? entity);
}

function normalizeWhitespace(text: string): string {
  return text.replace(/\s+/g, ' ').trim();
}

function decodeHtmlEntities(text: string): string {
  return text
    .replace(/&#(\d+);/g, (_match, codePoint) => String.fromCodePoint(Number.parseInt(codePoint, 10)))
    .replace(/&#x([0-9a-f]+);/gi, (_match, codePoint) => String.fromCodePoint(Number.parseInt(codePoint, 16)))
    .replace(/&(?:amp|lt|gt|quot|apos|nbsp|#39);/g, (entity) => HTML_ENTITIES[entity] ?? entity);
}

function escapeRegExp(text: string): string {
  return text.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function normalizeHtmlContent(text: string): string {
  return normalizeWhitespace(decodeHtmlEntities(text));
}

function stripYoutubeTitleSuffix(text: string): string {
  return normalizeWhitespace(text.replace(/\s*-\s*YouTube$/i, ''));
}

function normalizeComparableTitle(text: string): string {
  return stripYoutubeTitleSuffix(text).toLowerCase();
}

function isGenericYoutubeTitle(text: string): boolean {
  const normalized = normalizeComparableTitle(text);
  return (
    normalized === 'youtube'
    || normalized === 'youtube video'
    || normalized === 'youtube videos'
    || normalized === 'youtube 视频'
    || normalized === 'youtube 影片'
    || normalized === 'youtube 影集'
  );
}

function extractMetaTagContent(source: string, attribute: 'property' | 'name' | 'itemprop', value: string): string {
  const escapedValue = escapeRegExp(value);
  const patterns = [
    new RegExp(`<meta[^>]*${attribute}=["']${escapedValue}["'][^>]*content=["']([^"']+)["'][^>]*>`, 'i'),
    new RegExp(`<meta[^>]*content=["']([^"']+)["'][^>]*${attribute}=["']${escapedValue}["'][^>]*>`, 'i'),
  ];

  for (const pattern of patterns) {
    const match = source.match(pattern);
    if (match?.[1]) {
      return normalizeHtmlContent(match[1]);
    }
  }

  return '';
}

function extractLinkTagValue(
  source: string,
  attribute: 'itemprop' | 'rel',
  value: string,
  targetAttribute: 'href' | 'content',
): string {
  const escapedValue = escapeRegExp(value);
  const patterns = [
    new RegExp(`<link[^>]*${attribute}=["']${escapedValue}["'][^>]*${targetAttribute}=["']([^"']+)["'][^>]*>`, 'i'),
    new RegExp(`<link[^>]*${targetAttribute}=["']([^"']+)["'][^>]*${attribute}=["']${escapedValue}["'][^>]*>`, 'i'),
  ];

  for (const pattern of patterns) {
    const match = source.match(pattern);
    if (match?.[1]) {
      return normalizeHtmlContent(match[1]);
    }
  }

  return '';
}

function extractTitleTagContent(source: string): string {
  const match = source.match(/<title>([\s\S]*?)<\/title>/i);
  return match?.[1] ? stripYoutubeTitleSuffix(normalizeHtmlContent(match[1])) : '';
}

function extractWatchHtmlVideoDetails(source: string): YoutubeVideoDetails | undefined {
  const rawTitle = stripYoutubeTitleSuffix(
    extractMetaTagContent(source, 'property', 'og:title')
      || extractMetaTagContent(source, 'itemprop', 'name')
      || extractMetaTagContent(source, 'name', 'title')
      || extractTitleTagContent(source),
  );
  const title = rawTitle && !isGenericYoutubeTitle(rawTitle) ? rawTitle : '';
  const authorMatch = source.match(
    /<span[^>]*itemprop=["']author["'][^>]*>[\s\S]*?<link[^>]*itemprop=["']name["'][^>]*content=["']([^"']+)["']/i,
  );
  const author = authorMatch?.[1] ? normalizeHtmlContent(authorMatch[1]) : '';
  const thumbnailUrl = extractMetaTagContent(source, 'property', 'og:image')
    || extractLinkTagValue(source, 'itemprop', 'thumbnailUrl', 'href');

  if (!title && !author && !thumbnailUrl) {
    return undefined;
  }

  return {
    title: title || undefined,
    author: author || undefined,
    thumbnail: thumbnailUrl
      ? {
          thumbnails: [{ url: thumbnailUrl }],
        }
      : undefined,
  };
}

