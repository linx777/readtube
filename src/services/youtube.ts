function parseTranscriptXml(xml: string, languageCode: string): TranscriptSegment[] {
  const segments = [...xml.matchAll(XML_TEXT_PATTERN)].map((match) => ({
    text: match[3],
    duration: Number.parseFloat(match[2] ?? '0'),
    offset: Number.parseFloat(match[1] ?? '0'),
    lang: languageCode,
  }));

  if (!segments.length) {
    throw new AppError('empty_transcript', '字幕抓取成功，但没有解析到可读文本。', 422);
  }

  return segments;
}

function extractSegmentsFromBuffer(buffer: string, languageCode: string): { segments: TranscriptSegment[]; rest: string } {
  const segments: TranscriptSegment[] = [];
  let lastCompleteEnd = 0;

  XML_TEXT_PATTERN.lastIndex = 0;
  let match: RegExpExecArray | null;
  while ((match = XML_TEXT_PATTERN.exec(buffer))) {
    segments.push({
      text: match[3],
      duration: Number.parseFloat(match[2] ?? '0'),
      offset: Number.parseFloat(match[1] ?? '0'),
      lang: languageCode,
    });
    lastCompleteEnd = match.index + match[0].length;
  }

  return {
    segments,
    rest: lastCompleteEnd > 0 ? buffer.slice(lastCompleteEnd) : buffer,
  };
}

async function parseTranscriptResponse(
  response: Response,
  languageCode: string,
  callbacks?: TranscriptFetchCallbacks,
  previewMeta?: {
    videoId: string;
    sourceTitle: string;
    sourceAuthor: string;
    sourceLanguage: string;
    sourceLanguageCode: string;
    isAutoGenerated: boolean;
    thumbnailUrl: string;
  },
  signal?: AbortSignal,
): Promise<TranscriptSegment[]> {
  throwIfAborted(signal);

  if (!response.body) {
    return parseTranscriptXml(await response.text(), languageCode);
  }

  const reader = response.body.getReader();
  const decoder = new TextDecoder();
  const segments: TranscriptSegment[] = [];
  let buffer = '';
  let previewSent = false;
  let earlyWindowSent = false;
  const EARLY_WINDOW_SECONDS = 8 * 60;

  while (true) {
    throwIfAborted(signal);

    const { value, done } = await reader.read().catch((error: unknown) => {
      if (signal?.aborted || isAbortError(error)) {
        throw createAbortError(signal?.reason ?? error);
      }

      throw error;
    });
    if (done) {
      break;
    }

    buffer += decoder.decode(value, { stream: true });
    const extracted = extractSegmentsFromBuffer(buffer, languageCode);
    buffer = extracted.rest;
    segments.push(...extracted.segments);

    if (!previewSent && previewMeta) {
      const previewChunks = buildTranscriptChunks(segments);
      if (previewChunks.length >= 3) {
        previewSent = true;
        await callbacks?.onPreviewReady?.({
          ...previewMeta,
          transcriptPreview: buildTranscriptPreview(previewChunks, 6),
        });
      }
    }

    if (!earlyWindowSent && previewMeta) {
      const currentChunks = buildTranscriptChunks(segments);
      const earlyChunks = currentChunks.filter((chunk) => chunk.start < EARLY_WINDOW_SECONDS);
      const lastEarlyChunk = earlyChunks.at(-1);
      if (earlyChunks.length >= 6 && (lastEarlyChunk?.end ?? 0) >= EARLY_WINDOW_SECONDS) {
        earlyWindowSent = true;
        await callbacks?.onEarlyWindowReady?.({
          ...previewMeta,
          chunks: earlyChunks,
          transcriptText: buildTranscriptTextFromChunks(earlyChunks),
          windowEndLabel: formatTime(lastEarlyChunk?.end ?? EARLY_WINDOW_SECONDS),
        });
      }
    }
  }

  buffer += decoder.decode();
  const trailing = extractSegmentsFromBuffer(buffer, languageCode);
  segments.push(...trailing.segments);

  if (!segments.length) {
    throw new AppError('empty_transcript', '字幕抓取成功，但没有解析到可读文本。', 422);
  }

  if (!previewSent && previewMeta) {
    const previewChunks = buildTranscriptChunks(segments);
    if (previewChunks.length) {
      await callbacks?.onPreviewReady?.({
        ...previewMeta,
        transcriptPreview: buildTranscriptPreview(previewChunks, 6),
      });
    }
  }

  if (!earlyWindowSent && previewMeta) {
    const currentChunks = buildTranscriptChunks(segments);
    if (currentChunks.length) {
      const lastChunk = currentChunks.at(-1);
      await callbacks?.onEarlyWindowReady?.({
        ...previewMeta,
        chunks: currentChunks,
        transcriptText: buildTranscriptTextFromChunks(currentChunks),
        windowEndLabel: formatTime(lastChunk?.end ?? 0),
      });
    }
  }

  return segments;
}

async function fetchSupadataResponse(url: string, apiKey: string, signal?: AbortSignal): Promise<Response> {
  throwIfAborted(signal);

  let response: Response;
  try {
    response = await fetch(url, {
      method: 'GET',
      headers: {
        accept: 'application/json',
        'x-api-key': apiKey,
      },
      signal,
    });
  } catch (error: unknown) {
    if (signal?.aborted || isAbortError(error)) {
      throw createAbortError(signal?.reason ?? error);
    }

    throw new AppError('supadata_request_failed', '第三方字幕服务请求失败，请稍后重试。', 502, {
      cause: error,
    });
  }

  return response;
}

async function readSupadataBody(response: Response): Promise<SupadataTranscriptResponse> {
  let payload: unknown = null;
  try {
    payload = await response.json();
  } catch {
    payload = null;
  }

  return payload && typeof payload === 'object' && !Array.isArray(payload)
    ? payload as SupadataTranscriptResponse
    : {};
}

function mapSupadataError(response: Response, payload: SupadataTranscriptResponse): AppError {
  const errorText = normalizeWhitespace(
    typeof payload.error === 'string'
      ? payload.error
      : typeof payload.result === 'string'
        ? payload.result
        : '',
  );

  if (response.status === 401 || response.status === 403) {
    return new AppError(
      'video_unavailable',
      '这个视频需要公开可访问后才能提取字幕，第三方字幕服务也无法访问。',
      422,
    );
  }

  if (response.status === 404) {
    return new AppError('video_unavailable', '这个视频不存在、是私密视频，或当前不可公开访问。', 422);
  }

  if (response.status === 206) {
    return new AppError('captions_missing', '第三方字幕服务未找到可用字幕。', 422);
  }

  if (response.status === 429) {
    return new AppError('supadata_rate_limited', '第三方字幕服务当前限流，请稍后再试。', 429);
  }

  return new AppError(
    'supadata_request_failed',
    errorText || `第三方字幕服务请求失败。${response.status ? `（HTTP ${response.status} ${response.statusText || ''}）` : ''}`.trim(),
    response.status >= 400 && response.status < 600 ? response.status : 502,
  );
}

function buildSupadataTranscriptRequestUrl(input: string, mode: SupadataTranscriptMode): string {
  const url = new URL(`${SUPADATA_API_BASE_URL}/transcript`);
  url.searchParams.set('url', input);
  url.searchParams.set('text', 'false');
  url.searchParams.set('mode', mode);
  return url.toString();
}

function buildSupadataTranscriptJobUrl(jobId: string): string {
  return `${SUPADATA_API_BASE_URL}/transcript/${encodeURIComponent(jobId)}`;
}

function extractSupadataResolvedPayload(payload: SupadataTranscriptResponse): SupadataTranscriptResponse {
  const result = payload.result;
  if (result && typeof result === 'object' && !Array.isArray(result)) {
    return result as SupadataTranscriptResponse;
  }

  return payload;
}

async function resolveSupadataTranscriptPayload(
  input: string,
  apiKey: string,
  mode: SupadataTranscriptMode,
  signal?: AbortSignal,
): Promise<SupadataTranscriptResponse> {
  const initialResponse = await fetchSupadataResponse(buildSupadataTranscriptRequestUrl(input, mode), apiKey, signal);
  const initialPayload = await readSupadataBody(initialResponse);

  if (initialResponse.status === 200) {
    return extractSupadataResolvedPayload(initialPayload);
  }

  if (initialResponse.status !== 202) {
    throw mapSupadataError(initialResponse, initialPayload);
  }

  const jobId = typeof initialPayload.jobId === 'string' ? initialPayload.jobId.trim() : '';
  if (!jobId) {
    throw new AppError('supadata_invalid_job', '第三方字幕服务返回了无法识别的任务结果。', 502);
  }

  for (let attempt = 1; attempt <= SUPADATA_MAX_POLL_ATTEMPTS; attempt += 1) {
    throwIfAborted(signal);
    await sleep(SUPADATA_POLL_INTERVAL_MS, signal);

    const jobResponse = await fetchSupadataResponse(buildSupadataTranscriptJobUrl(jobId), apiKey, signal);
    const jobPayload = await readSupadataBody(jobResponse);

    if (!jobResponse.ok) {
      throw mapSupadataError(jobResponse, jobPayload);
    }

    const status = normalizeWhitespace(typeof jobPayload.status === 'string' ? jobPayload.status : '').toLowerCase();
    if (status === 'completed') {
      return extractSupadataResolvedPayload(jobPayload);
    }

    if (status === 'failed') {
      throw new AppError(
        'supadata_job_failed',
        normalizeWhitespace(typeof jobPayload.error === 'string' ? jobPayload.error : '') || '第三方字幕服务处理失败，请稍后重试。',
        502,
      );
    }

    if (status !== 'queued' && status !== 'active') {
      throw new AppError('supadata_invalid_job', '第三方字幕服务返回了未知的任务状态。', 502);
    }
  }

  throw new AppError('supadata_timeout', '第三方字幕服务处理超时，请稍后重试。', 504);
}

function normalizeSupadataSegments(content: unknown, fallbackLanguageCode: string): TranscriptSegment[] {
  if (typeof content === 'string') {
    const text = normalizeWhitespace(content);
    return text
      ? [{ text, duration: 0, offset: 0, lang: fallbackLanguageCode }]
      : [];
  }

  if (!Array.isArray(content)) {
    return [];
  }

  return content.flatMap((item) => {
    if (!item || typeof item !== 'object' || Array.isArray(item)) {
      return [];
    }

    const segment = item as SupadataTranscriptSegment;
    const text = normalizeWhitespace(typeof segment.text === 'string' ? segment.text : '');
    if (!text) {
      return [];
    }

    const offsetMs = typeof segment.offset === 'number' ? segment.offset : Number(segment.offset ?? 0);
    const durationMs = typeof segment.duration === 'number' ? segment.duration : Number(segment.duration ?? 0);
    const languageCode = normalizeWhitespace(
      typeof segment.lang === 'string' ? segment.lang : fallbackLanguageCode,
    ) || fallbackLanguageCode;

    return [{
      text,
      duration: Number.isFinite(durationMs) ? Math.max(0, durationMs / 1000) : 0,
      offset: Number.isFinite(offsetMs) ? Math.max(0, offsetMs / 1000) : 0,
      lang: languageCode,
    }];
  });
}

async function emitPreparedTranscriptCallbacks(
  chunks: TranscriptChunk[],
  callbacks: TranscriptFetchCallbacks | undefined,
  previewMeta: {
    videoId: string;
    sourceTitle: string;
    sourceAuthor: string;
    sourceLanguage: string;
    sourceLanguageCode: string;
    isAutoGenerated: boolean;
    thumbnailUrl: string;
  },
): Promise<void> {
  if (!chunks.length) {
    return;
  }

  await callbacks?.onPreviewReady?.({
    ...previewMeta,
    transcriptPreview: buildTranscriptPreview(chunks, 6),
  });

  const EARLY_WINDOW_SECONDS = 8 * 60;
  const earlyChunks = chunks.filter((chunk) => chunk.start < EARLY_WINDOW_SECONDS);
  const fallbackChunks = earlyChunks.length ? earlyChunks : chunks;
  const lastChunk = fallbackChunks.at(-1);

  await callbacks?.onEarlyWindowReady?.({
    ...previewMeta,
    chunks: fallbackChunks,
    transcriptText: buildTranscriptTextFromChunks(fallbackChunks),
    windowEndLabel: formatTime(lastChunk?.end ?? 0),
  });
}

