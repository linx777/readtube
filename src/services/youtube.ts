function resolveThumbnailUrl(videoId: string, details?: YoutubeVideoDetails, explicitUrl?: string): string {
  const trimmedExplicitUrl = explicitUrl?.trim();
  if (trimmedExplicitUrl) {
    return trimmedExplicitUrl;
  }

  const thumbnails = details?.thumbnail?.thumbnails ?? [];
  const detailThumbnailUrl = thumbnails.at(-1)?.url?.trim();
  if (detailThumbnailUrl) {
    return detailThumbnailUrl;
  }

  return `https://i.ytimg.com/vi/${videoId}/hqdefault.jpg`;
}

