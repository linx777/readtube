async function fetchYoutubeTranscriptPlusRequest(params: YoutubeTranscriptFetchParams): Promise<Response> {
  const headers = buildYoutubeHeaders({
    headers: {
      ...(params.headers ?? {}),
      referer: 'https://www.youtube.com/',
      ...(params.lang ? { 'accept-language': params.lang } : {}),
      ...(params.userAgent ? { 'user-agent': params.userAgent } : {}),
    },
  });

  try {
    return await fetch(params.url, {
      method: params.method ?? 'GET',
      headers,
      body: params.body,
      signal: params.signal,
    });
  } catch (error: unknown) {
    if (params.signal?.aborted || isAbortError(error)) {
      throw createAbortError(params.signal?.reason ?? error);
    }

    throw error;
  }
}

async function fetchYoutubeTranscriptPlusWatchPage(
  params: YoutubeTranscriptFetchParams,
): Promise<Response> {
  const videoId = extractVideoId(params.url);
  let lastResponse: Response | null = null;
  let lastBody = '';

  for (const url of buildVideoBootstrapUrls(videoId)) {
    const response = await fetchYoutubeTranscriptPlusRequest({
      ...params,
      url,
      method: 'GET',
      body: undefined,
    });
    const body = await response.text();

    if (
      response.ok
      && (
        body.includes('"INNERTUBE_API_KEY"')
        || body.includes('INNERTUBE_API_KEY\\":\\"')
        || body.includes('g-recaptcha')
      )
    ) {
      return cloneTextResponse(response, body);
    }

    lastResponse = response;
    lastBody = body;
  }

  if (lastResponse) {
    return cloneTextResponse(lastResponse, lastBody);
  }

  return new Response('', { status: 502, statusText: 'Bad Gateway' });
}

function mapYoutubeTranscriptPlusError(error: unknown, videoId: string): AppError {
  if (error instanceof AppError) {
    return error;
  }

  if (error instanceof YoutubeTranscriptInvalidVideoIdError) {
    return new AppError('invalid_video_id', '这不是一个合法的 YouTube 视频链接。', 400, { cause: error });
  }

  if (error instanceof YoutubeTranscriptTooManyRequestError) {
    return new AppError('youtube_rate_limited', 'YouTube 暂时触发了风控，请稍后再试。', 429, { cause: error });
  }

  if (error instanceof YoutubeTranscriptVideoUnavailableError) {
    return new AppError('video_unavailable', '这个视频当前不可访问，无法提取字幕。', 422, { cause: error });
  }

  if (error instanceof YoutubeTranscriptDisabledError) {
    return new AppError('captions_disabled', '这个视频没有开放可抓取字幕。', 422, { cause: error });
  }

  if (error instanceof YoutubeTranscriptNotAvailableLanguageError) {
    return new AppError('captions_missing', '这个视频没有所需语言的字幕轨道。', 422, { cause: error });
  }

  if (error instanceof YoutubeTranscriptNotAvailableError) {
    return new AppError('captions_missing', '这个视频没有可用字幕轨道。', 422, { cause: error });
  }

  if (error instanceof Error) {
    return new AppError('youtube_fetch_failed', error.message || '抓取 YouTube 信息失败，请稍后重试。', 502, {
      cause: error,
    });
  }

  return new AppError('youtube_fetch_failed', '抓取 YouTube 信息失败，请稍后重试。', 502, {
    cause: error,
  });
}

async function fetchResponse(url: string, init?: RequestInit): Promise<Response> {
  throwIfAborted(init?.signal);
  let lastStatus = 0;
  let lastStatusText = '';

  for (let attempt = 1; attempt <= YOUTUBE_FETCH_MAX_ATTEMPTS; attempt += 1) {
    throwIfAborted(init?.signal);

    let response: Response;
    try {
      response = await fetch(url, {
        ...init,
        headers: buildYoutubeHeaders(init),
      });
    } catch (error: unknown) {
      if (init?.signal?.aborted || isAbortError(error)) {
        throw createAbortError(init?.signal?.reason ?? error);
      }

      console.warn('[youtube] fetch_exception', JSON.stringify({
        url,
        attempt,
        error: error instanceof Error ? error.message : String(error),
      }));

      if (attempt < YOUTUBE_FETCH_MAX_ATTEMPTS) {
        await sleep(YOUTUBE_FETCH_RETRY_BASE_MS * attempt, init?.signal ?? undefined);
        continue;
      }

      throw error;
    }

    if (response.ok) {
      return response;
    }

    lastStatus = response.status;
    lastStatusText = response.statusText;
    console.warn('[youtube] fetch_non_ok', JSON.stringify({
      url,
      attempt,
      status: response.status,
      statusText: response.statusText,
    }));
    await response.body?.cancel().catch(() => {});

    if (attempt < YOUTUBE_FETCH_MAX_ATTEMPTS && shouldRetryYoutubeStatus(response.status)) {
      await sleep(YOUTUBE_FETCH_RETRY_BASE_MS * attempt, init?.signal ?? undefined);
      continue;
    }

    break;
  }

  if (lastStatus === 429) {
    throw new AppError('youtube_rate_limited', 'YouTube 暂时触发了风控，请稍后再试。', 429);
  }

  throw new AppError(
    'youtube_fetch_failed',
    `抓取 YouTube 信息失败，请稍后重试。${lastStatus ? `（HTTP ${lastStatus}${lastStatusText ? ` ${lastStatusText}` : ''}）` : ''}`,
    502,
  );
}

