function createBundle(
  overrides: Partial<TranscriptBundle> = {},
): TranscriptBundle {
  const chunks = overrides.chunks ?? [
    { start: 0, end: 30, text: 'Opening line' },
    { start: 60, end: 90, text: 'Second line' },
  ];

  return {
    videoId: 'abc123xyz00',
    sourceTitle: 'Test title',
    sourceAuthor: 'Test author',
    channelId: 'channel',
    languageCode: 'en',
    languageName: 'English',
    isAutoGenerated: true,
    durationSeconds: overrides.durationSeconds ?? Math.ceil(chunks.at(-1)?.end ?? 0),
    viewCount: 10,
    thumbnailUrl: 'https://example.com/thumb.jpg',
    segments: [],
    chunks,
    transcriptText: overrides.transcriptText ?? chunks.map((chunk) => chunk.text).join('\n'),
    ...overrides,
  };
}

function createDialogueSlice(
  overrides: {
    section?: Partial<TranscriptDialogueSliceResult['section']>;
    turns?: TranscriptDialogueSliceResult['turns'];
    groups?: TranscriptDialogueSliceResult['groups'];
    model?: string;
  } = {},
): TranscriptDialogueSliceResult {
  const turns = overrides.turns ?? [
    { timestamp: '00:00', speaker: 'Host', textZh: 'Example line' },
  ];
  return {
    section: {
      startLabel: '00:00',
      endLabel: '01:00',
      subtitle: 'Section',
      summary: 'Summary',
      transcript: '[00:00] Example line',
      ...overrides.section,
    },
    turns,
    groups: overrides.groups ?? [],
    model: overrides.model ?? 'test-model',
  };
}

