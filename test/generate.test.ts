describe('buildFirstCallTranscriptBundle', () => {
  it('keeps the full transcript when duration does not exceed 30 minutes', () => {
    const bundle = createBundle({
      durationSeconds: 20 * 60,
    });

    expect(buildFirstCallTranscriptBundle(bundle)).toBe(bundle);
  });

  it('slices the transcript to the first 30 minutes when duration exceeds the limit', () => {
    const bundle = createBundle({
      durationSeconds: 75 * 60,
      chunks: [
        { start: 0, end: 60, text: 'Opening line' },
        { start: 29 * 60 + 45, end: 30 * 60 + 20, text: 'Still inside the first half hour' },
        { start: 30 * 60, end: 30 * 60 + 30, text: 'Should be excluded' },
        { start: 50 * 60, end: 50 * 60 + 30, text: 'Later line' },
      ],
    });

    expect(buildFirstCallTranscriptBundle(bundle)).toEqual({
      ...bundle,
      durationSeconds: 30 * 60,
      chunks: [
        { start: 0, end: 60, text: 'Opening line' },
        { start: 29 * 60 + 45, end: 30 * 60, text: 'Still inside the first half hour' },
      ],
      transcriptText: 'Opening line\nStill inside the first half hour',
    });
  });
});

